void LIS2DU12_Init(void)
{
    HAL_Delay(50);

    uint8_t data;

    /* Basic configuration */
    data = 0x37;
    HAL_I2C_Mem_Write(&hi2c3,
                      LIS2DU12_ADDR,
                      CTRL1_REG,
                      I2C_MEMADD_SIZE_8BIT,
                      &data,
                      1,
                      100);

    /* High performance mode */
    data = 0x20;
    HAL_I2C_Mem_Write(&hi2c3,
                      LIS2DU12_ADDR,
                      CTRL3_REG,
                      I2C_MEMADD_SIZE_8BIT,
                      &data,
                      1,
                      100);

    /* Enable BDU (Block Data Update) */
    data = 0x08;
    HAL_I2C_Mem_Write(&hi2c3,
                      LIS2DU12_ADDR,
                      CTRL4_REG,
                      I2C_MEMADD_SIZE_8BIT,
                      &data,
                      1,
                      100);

    /* ODR = 100 Hz, FS = ±2g */
    data = 0x80;

    HAL_I2C_Mem_Write(&hi2c3,
                      LIS2DU12_ADDR,
                      CTRL5_REG,
                      I2C_MEMADD_SIZE_8BIT,
                      &data,
                      1,
                      100);
    HAL_Delay(50);

    printf("LIS2DU12 Init done\r\n");
}

void LIS2DU12_ReadAccel(void)
{
    uint8_t buffer[6];
    int16_t raw_x, raw_y, raw_z;

    HAL_I2C_Mem_Read(&hi2c3,
                     LIS2DU12_ADDR,
                     OUTX_L_REG | 0x80,   // auto increment
                     I2C_MEMADD_SIZE_8BIT,
                     buffer,
                     6,
                     100);

    raw_x = (int16_t)((buffer[1] << 8) | buffer[0]);
    raw_y = (int16_t)((buffer[3] << 8) | buffer[2]);
    raw_z = (int16_t)((buffer[5] << 8) | buffer[4]);

//    printf("RAW X:%d  Y:%d  Z:%d\r\n", raw_x, raw_y, raw_z);

    float ax = raw_x * 0.061f / 1000.0f;
    float ay = raw_y * 0.061f / 1000.0f;
    float az = raw_z * 0.061f / 1000.0f;

    printf("X: %.3f g  Y: %.3f g  Z: %.3f g\r\n\n", ax, ay, az);
}


